#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const INPUT_DIR = path.join(ROOT, "data", "growth");
const OUTPUT_FILE = path.join(ROOT, "src", "data", "generated", "topPrisonEntityCohort.generated.ts");
const COHORT_LIMIT = 100;

function parseCsv(text) {
  const lines = text.split(/\r?\n/).filter(Boolean);
  if (lines.length < 2) return [];
  const headers = lines[0].split(",").map((h) => h.trim().toLowerCase());
  return lines.slice(1).map((line) => {
    const cols = line.split(",").map((c) => c.trim());
    const row = {};
    headers.forEach((h, i) => {
      row[h] = cols[i] ?? "";
    });
    return row;
  });
}

function safeNum(value) {
  const n = Number(String(value ?? "").replace(/[^\d.-]/g, ""));
  return Number.isFinite(n) ? n : 0;
}

function parsePrisonPath(urlOrPath) {
  try {
    const asUrl = urlOrPath.startsWith("http") ? new URL(urlOrPath).pathname : urlOrPath;
    const parts = asUrl.split("/").filter(Boolean);
    if (parts.length < 3) return null;
    if (parts[0] !== "prisons") return null;
    return { countrySlug: parts[1], prisonSlug: parts[2] };
  } catch {
    return null;
  }
}

function buildRows(rows, sourceName) {
  return rows
    .map((row) => {
      const page = row.page || row.url || row.path || "";
      const parsed = parsePrisonPath(page);
      if (!parsed) return null;
      const impressions = safeNum(row.impressions);
      const clicks = safeNum(row.clicks);
      const users = safeNum(row.users) || safeNum(row.active_users);
      const sessions = safeNum(row.sessions);
      const rpm = safeNum(row.rpm) || safeNum(row.adsense_rpm);
      const score = impressions * 0.35 + clicks * 1.25 + users * 0.9 + sessions * 0.8 + rpm * 2;
      return {
        countrySlug: parsed.countrySlug,
        prisonSlug: parsed.prisonSlug,
        score: Math.round(score * 100) / 100,
        source: sourceName,
      };
    })
    .filter(Boolean);
}

function run() {
  if (!fs.existsSync(INPUT_DIR)) {
    fs.writeFileSync(
      OUTPUT_FILE,
      `/* eslint-disable */\n/** Auto-generated by scripts/build-growth-cohort.mjs — do not edit by hand */\n\n` +
        `export type TopPrisonEntityCohortRow = {\n  countrySlug: string;\n  prisonSlug: string;\n  score: number;\n  source: string;\n  notes?: string;\n};\n\n` +
        `export const topPrisonEntityCohortGenerated: TopPrisonEntityCohortRow[] = [];\n`,
      "utf8",
    );
    console.log("No data/growth directory found. Wrote empty cohort file.");
    return;
  }

  const files = fs
    .readdirSync(INPUT_DIR)
    .filter((name) => name.endsWith(".csv"))
    .map((name) => path.join(INPUT_DIR, name));

  const combined = [];
  for (const file of files) {
    const text = fs.readFileSync(file, "utf8");
    const rows = parseCsv(text);
    combined.push(...buildRows(rows, path.basename(file)));
  }

  const merged = new Map();
  for (const row of combined) {
    const key = `${row.countrySlug}/${row.prisonSlug}`;
    const prev = merged.get(key);
    if (!prev || row.score > prev.score) merged.set(key, row);
  }

  const out = Array.from(merged.values())
    .sort((a, b) => b.score - a.score)
    .slice(0, COHORT_LIMIT);

  const body = [
    "/* eslint-disable */",
    "/** Auto-generated by scripts/build-growth-cohort.mjs — do not edit by hand */",
    "",
    "export type TopPrisonEntityCohortRow = {",
    "  countrySlug: string;",
    "  prisonSlug: string;",
    "  score: number;",
    "  source: string;",
    "  notes?: string;",
    "};",
    "",
    "export const topPrisonEntityCohortGenerated: TopPrisonEntityCohortRow[] = [",
    ...out.map(
      (row) =>
        `  { countrySlug: ${JSON.stringify(row.countrySlug)}, prisonSlug: ${JSON.stringify(row.prisonSlug)}, score: ${row.score}, source: ${JSON.stringify(row.source)} },`,
    ),
    "];",
    "",
  ].join("\n");

  fs.writeFileSync(OUTPUT_FILE, body, "utf8");
  console.log(`Wrote ${out.length} rows to ${path.relative(ROOT, OUTPUT_FILE)}.`);
}

run();

